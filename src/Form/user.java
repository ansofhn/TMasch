    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        String sql = "insert into users values (?,?,?,?,?,?)";
        try{
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, txtid.getText());
            stat.setString(2, txtusrn.getText());
            String password = new String(jpw.getPassword());
            stat.setString(3, password);
            stat.setString(4, txtnml.getText());
            String roleStr = jrole.getSelectedItem().toString();
            int roleInt;        
            switch (roleStr) {
            case "Admin": roleInt = 1; break;
            case "Guru":  roleInt = 2; break;
            case "Siswa": roleInt = 3; break;
            default:      roleInt = 0; // Or handle error
            }
            stat.setInt(5, roleInt);
            stat.setString(6, txtdept.getText());

            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "data berhasil disimpan");
            kosong();
            txtid.requestFocus();
        }
        catch (SQLException e){
            JOptionPane.showMessageDialog(null, "data gagal disimpan"+e);
        }
        datatable();
    // TODO add your handling code here:
    }//GEN-LAST:event_bsimpanActionPerformed

