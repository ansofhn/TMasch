    private void tblusrMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblusrMouseClicked
        int bar = tblusr.getSelectedRow();
        txtid.setText(tabmode.getValueAt(bar, 0).toString());
        txtusrn.setText(tabmode.getValueAt(bar, 1).toString());
        jpw.setText(tabmode.getValueAt(bar, 2).toString());
        txtnml.setText(tabmode.getValueAt(bar, 3).toString());
        txtdept.setText(tabmode.getValueAt(bar, 5).toString());
        
        int roleId = Integer.parseInt(tabmode.getValueAt(bar, 4).toString());
        switch (roleId) {
        case 1: jrole.setSelectedItem("Admin"); break;
        case 2: jrole.setSelectedItem("Guru");  break;
        case 3: jrole.setSelectedItem("Siswa"); break;
        default: jrole.setSelectedIndex(0);     break;
    }
    }//GEN-LAST:event_tblusrMouseClicked

